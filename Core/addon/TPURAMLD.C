#define ITC		11		/* Input Capture/Input Transition Counter */


void ClearRAM ()
{				/* clear only possible before EMU bit in TPU MCR is set */
	int i;

	for (i = 0; i < RAMSIZE; i += 4)
		*(long *)(RAMSTART + i) = 0L;
}

void LoadEmulationRAM (const uchar code[])
{
	uint offset, count, i;

	count  = (code[0] << 8) + code[1];
	offset = (code[2] << 8) + code[3];
	code += 4;
	for (i = 0; i < count; i++)
		*(uchar *)(RAMSTART + i + offset) = code[i];
}

